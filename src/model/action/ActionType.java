package model.action;

/**
 * Types of actions that a bacteria can choose.
 */
public enum ActionType {
    /**
     * Action for moving.
     */
    MOVE,
    /**
     * Action for eating.
     */
    EAT,
    /**
     * Action for replicating.
     */
    REPLICATE,
    /**
     * Action for comunicating.
     */
    COMUNICATE,
    /**
     * Action for doing nothing.
     */
    NOTHING;
}
