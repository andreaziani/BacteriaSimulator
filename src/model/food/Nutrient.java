package model.food;

/**
 * All possible type of nutrients.
 * 
 */
public enum Nutrient {
    /**
     * Water.
     */
    WATER,
    /**
     * Carbohydrates.
     */
    CARBOHYDRATES,
    /**
     * Peptones.
     */
    PEPTONES,
    /**
     * Hydrolysates.
     */
    HYDROLYSATES,
    /**
     * Inorganic salt.
     */
    INORGANIC_SALT,
    /**
     * Vitamines.
     */
    VITAMINES,
    /**
     * Minerals.
     */
    MINERALS,
    /**
     * Proteins.
     */
    PROTEINS;
}
