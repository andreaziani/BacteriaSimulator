package model.bacteria.behavior.decisionmaker;
/**
 * enumeration representing all types of options for a DecisionMaker concerning the ActionType EAT.
 */
public enum EatingDecisionOption {
    /**
     * Represents an AlwaysEatDecisionMaker.
     */
    ALWAYS_EAT,
    /**
     * Represents a PreferentialEatingDecisionMaker.
     */
    PREFERENTIAL_EATING;
}
