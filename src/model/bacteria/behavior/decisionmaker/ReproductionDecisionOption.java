package model.bacteria.behavior.decisionmaker;
/**
 * enumeration representing all types of options for a DecisionMaker concerning the ActionType REPRODUCE.
 */
public enum ReproductionDecisionOption {
    /**
     * Represents an AlwaysReproduceDecisionMaker.
     */
    ALWAYS_REPRODUCE,
    /**
     * Represents a RandomReproductionDecisionMaker.
     */
    RANDOM_REPRODUCTION;
}
